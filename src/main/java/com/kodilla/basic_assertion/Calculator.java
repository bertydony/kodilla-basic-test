package com.kodilla.basic_assertion;
import java.lang.Math;
public class Calculator {
    public int sum(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public int squared(int a, int n) {
        return (int) Math.pow(a, n);
    }
}
